    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        int linha = listMembros.size() - 1;
        listaMembros.setSelectionInterval(linha, linha);
        Membro m = new Membro(txtNome.getText(), Integer.parseInt(txtIni.getText()), Integer.parseInt(txtCA.getText()), Integer.parseInt(txtVida.getText()));
        dao.salvar(m);
        txtIni.setText("");
        txtNome.setText("");
        txtCA.setText("0");
        txtVida.setText("0");
        atualizaTabela();
    }//GEN-LAST:event_addButtonActionPerformed

    private void NextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NextActionPerformed
        listaMembros.clearSelection();
        atualizaTabela();
        if (index == listMembros.size() - 1) {
            index = 0;
            listaMembros.setSelectedIndex(index);
//            txtTurnoAtual.setText(listMembros.get(index).getNome());
        } else {
            listaMembros.setSelectedIndex(++index);
//            txtTurnoAtual.setText(listMembros.get(index).getNome());
        }

    }//GEN-LAST:event_NextActionPerformed

